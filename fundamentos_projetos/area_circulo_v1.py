#Nesse desafio vamos utilizar a formula da área de uma circunferência
raio = 15
pi = 3.14159

#Primeira forma colocando o resultado diretamente dentro de uma variável
area = pi * raio**2
print(f'A área da circunferência de raio 5 é: {area}')

#Segunda forma sem utilizar uma váriavel area
print(f'A área da circunferência de raio 5 é: {pi * raio**2}')